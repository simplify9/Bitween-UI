import {KeyValuePair, OptionType} from "src/types/common";
import Button from "./Button";
import React, {useState} from "react";
import Modal from "../Modal";
import FormField from "./FormField";
import TextEditor from "./TextEditor";
import {ChoiceEditor} from "./ChoiceEditor";
import {MdLock, MdModeEditOutline, MdOutlineRemoveCircle} from "react-icons/md";
import {HiPlusCircle} from "react-icons/hi"

const PRIVATE_SENTINEL = "__private__";

type Props = Omit<React.JSX.IntrinsicElements['input'], "onChange"> & {
    onAdd?: (value: KeyValuePair) => void
    onRemove?: (value: KeyValuePair) => void
    values?: KeyValuePair[]
    title?: string,
    keyLabel?: string,
    valueLabel?: string
    valueAutoGeneratedOnAdd?: boolean
    addLabel?: string
    keyOptions?: OptionType[]
    onEdit?: (value: KeyValuePair) => void
    hideEmptyLabel?: boolean
    valueRenderer?: (value: string, onChange: (v: string) => void) => React.ReactNode
    validate?: (value: KeyValuePair) => string | null
    valuePlaceholder?: string
    keyPlaceholder?: string
    description?: string
    autoGeneratedValueHint?: string
    onAddWithReveal?: (value: KeyValuePair) => Promise<string | null>
    onSaveAfterReveal?: () => Promise<void>
    privateKeys?: Set<string>;
}

const Component: React.FC<Props> = ({

                                        values,
                                        onAdd,
                                        onRemove,
                                        title,
                                        keyLabel,
                                        valueLabel,
                                        addLabel,
                                        valueAutoGeneratedOnAdd,
                                        keyOptions,
                                        hideEmptyLabel,
                                        onEdit,
                                        valueRenderer,
                                        validate,
                                        valuePlaceholder,
                                        keyPlaceholder,
                                        description,
                                        autoGeneratedValueHint,
                                        onAddWithReveal,
                                        onSaveAfterReveal,
                                        privateKeys
                                    }) => {

    const [mode, setMode] = useState<"NONE" | "EDIT" | "ADD">("NONE");
    const [modalError, setModalError] = useState<string | null>(null);
    const [isReplacingPrivate, setIsReplacingPrivate] = useState(false);
    const [originalValue, setOriginalValue] = useState('');

    const [newKeyValue, setNewKeyValue] = useState<KeyValuePair>({
        key: '',
        value: ''
    });
    const [revealValue, setRevealValue] = useState<string | null>(null);
    const [copied, setCopied] = useState(false);
    const [isGenerating, setIsGenerating] = useState(false);

    const onCloseModal = () => {
        setMode("NONE")
        setModalError(null)
        setRevealValue(null)
        setCopied(false)
        setIsReplacingPrivate(false)
        setOriginalValue('')
        setNewKeyValue({
            key: '',
            value: ''
        })
    }

    const onAddSubmit = async () => {
        if (newKeyValue) {
            if (validate) {
                const error = validate(newKeyValue);
                if (error) {
                    setModalError(error);
                    return;
                }
            }
            setModalError(null);
            if (onAddWithReveal) {
                setIsGenerating(true);
                const generated = await onAddWithReveal(newKeyValue);
                setIsGenerating(false);
                if (generated) {
                    setRevealValue(generated);
                } else {
                    setModalError("Failed to generate API key. Please try again.");
                }
            } else {
                onAdd!(newKeyValue)
                setNewKeyValue({key: '', value: ''})
                onCloseModal()
            }
        }
    }

    const onRevealSave = async () => {
        await onSaveAfterReveal?.();
        onCloseModal();
    }

    const handleCopy = () => {
        if (revealValue) {
            navigator.clipboard.writeText(revealValue);
            setCopied(true);
            setTimeout(() => setCopied(false), 2000);
        }
    }
    const onEditSubmit = () => {
        if (newKeyValue) {
            if (validate) {
                const error = validate(newKeyValue);
                if (error) {
                    setModalError(error);
                    return;
                }
            }
            setModalError(null);
            onEdit?.(newKeyValue)
            setNewKeyValue({key: '', value: ''})
            onCloseModal()
        }
    }

    const onPressEdit = (value: KeyValuePair) => {
        setNewKeyValue(value)
        setOriginalValue(value.value)
        setIsReplacingPrivate(false)
        setMode("EDIT")
    }

    const isSubmitDisabled = (() => {
        if (isGenerating) return true;
        if (revealValue) return false;
        if (mode === "ADD") {
            if (!newKeyValue.key) return true;
            if (!valueAutoGeneratedOnAdd && !newKeyValue.value) return true;
            return false;
        }
        if (mode === "EDIT") {
            const isPrivate = privateKeys?.has(newKeyValue.key);
            if (isPrivate && !isReplacingPrivate) return true;
            if (isPrivate && isReplacingPrivate) return !newKeyValue.value.trim();
            return newKeyValue.value === originalValue;
        }
        return false;
    })();

    return (
        <div className={"w-100 w-full bg-white "}>
            {(['EDIT', 'ADD'].includes(mode)) &&
                <Modal onClose={onCloseModal}
                       submitLabel={revealValue ? "Save" : (mode === "ADD" && onAddWithReveal ? "Generate Key" : mode === "EDIT" ? "Update" : "Add")}
                       submitDisabled={isSubmitDisabled}
                       onSubmit={revealValue ? onRevealSave : () => mode === "EDIT" ? onEditSubmit() : onAddSubmit()}>
                    <div className="flex flex-col gap-5">
                        {revealValue ? (
                            <>
                                <div className="border-b border-gray-100 pb-3">
                                    <h2 className="text-base font-semibold text-gray-800">API Key Generated</h2>
                                    <p className="mt-1 text-xs text-gray-500">
                                        Copy this key now — it won't be shown again once you close this dialog.
                                    </p>
                                </div>
                                <div className="flex flex-col gap-2">
                                    <span className="text-xs font-medium text-gray-600">{newKeyValue.key}</span>
                                    <div className="flex items-center gap-2 rounded-md border border-gray-200 bg-gray-50 px-3 py-2">
                                        <code className="flex-1 break-all text-xs font-mono text-gray-800 select-all">{revealValue}</code>
                                        <button
                                            type="button"
                                            onClick={handleCopy}
                                            className="shrink-0 rounded px-2 py-1 text-xs font-medium text-primary-600 hover:bg-gray-100 transition-colors"
                                        >
                                            {copied ? "Copied!" : "Copy"}
                                        </button>
                                    </div>
                                </div>
                                <div className="rounded-md border border-amber-100 bg-amber-50 px-3 py-2 text-xs text-amber-800">
                                    Click <strong>Save</strong> to persist immediately, or <strong>Cancel</strong> to save later using the page Save button.
                                </div>
                            </>
                        ) : (
                            <>
                                {/* Header */}
                                <div className="border-b border-gray-100 pb-3">
                                    <h2 className="text-base font-semibold text-gray-800">{addLabel}</h2>
                                    {description && (
                                        <p className="mt-1 text-xs normal-case font-normal tracking-normal text-gray-500">
                                            {description}
                                        </p>
                                    )}
                                </div>

                                {/* Inline error */}
                                {modalError && (
                                    <div className="flex items-start gap-2 bg-red-50 border border-red-200 rounded-md px-3 py-2">
                                        <span className="text-xs text-red-600">{modalError}</span>
                                    </div>
                                )}

                                {/* Key field */}
                                <FormField title={keyLabel ?? ""} className="grow">
                                    {(!keyOptions || mode === "EDIT") &&
                                        <TextEditor
                                            disabled={mode === "EDIT"}
                                            placeholder={keyPlaceholder ?? `e.g. Customer Name`}
                                            value={newKeyValue.key}
                                            onChange={(t) => setNewKeyValue({...newKeyValue, key: t})}/>
                                    }
                                    {(keyOptions && mode !== "EDIT") &&
                                        <ChoiceEditor
                                            options={keyOptions}
                                            optionValue={(item) => item.id}
                                            optionTitle={(item) => item.title}
                                            value={newKeyValue.key}
                                            autoFocus={true}
                                            disabled={keyOptions?.length === 0}
                                            onChange={(t) => setNewKeyValue({...newKeyValue, key: t})}/>
                                    }
                                </FormField>

                                {/* Secure prop warning */}
                                {privateKeys?.has(newKeyValue.key) && (
                                    <div className="flex items-start gap-2 rounded-md border border-amber-200 bg-amber-50 px-3 py-2">
                                        <MdLock className="mt-0.5 shrink-0 text-amber-600" size={14}/>
                                        <span className="text-xs text-amber-800">
                                            This is a secure property. Once you click <strong>{mode === "EDIT" ? "Update" : "Add"}</strong>, the value will not be visible again.
                                        </span>
                                    </div>
                                )}

                                {/* Value field */}
                                {!valueAutoGeneratedOnAdd && (
                                    <FormField title={valueLabel ?? ""} className="grow">
                                        {mode === "EDIT" && privateKeys?.has(newKeyValue.key) && !isReplacingPrivate ? (
                                            <div className="flex items-center justify-between gap-3 rounded-md border border-gray-200 bg-gray-50 px-3 py-2">
                                                <div className="flex items-center gap-2 text-gray-400">
                                                    <MdLock size={13}/>
                                                    <span className="tracking-widest text-sm">••••••••••••</span>
                                                </div>
                                                <button
                                                    type="button"
                                                    onClick={() => {
                                                        setNewKeyValue({...newKeyValue, value: ''});
                                                        setIsReplacingPrivate(true);
                                                    }}
                                                    className="shrink-0 rounded px-2 py-1 text-xs font-medium text-primary-600 border border-primary-200 hover:bg-primary-50 transition-colors"
                                                >
                                                    Replace
                                                </button>
                                            </div>
                                        ) : (
                                            <>
                                                {valueRenderer
                                                    ? valueRenderer(newKeyValue.value, (v) => setNewKeyValue({...newKeyValue, value: v}))
                                                    : <textarea
                                                        rows={3}
                                                        placeholder={valuePlaceholder ?? `Enter ${valueLabel ?? 'value'}…`}
                                                        className="w-full border border-gray-300 rounded-md shadow-sm p-2 text-sm font-mono focus:outline-none focus:ring-2 focus:ring-primary-400 resize-y"
                                                        onChange={(t) => setNewKeyValue({...newKeyValue, value: t.target.value})}
                                                        value={newKeyValue?.value}
                                                        autoFocus={isReplacingPrivate}/>
                                                }
                                            </>
                                        )}
                                    </FormField>
                                )}
                                {valueAutoGeneratedOnAdd && autoGeneratedValueHint && !onAddWithReveal && (
                                    <div className="rounded-md border border-blue-100 bg-blue-50 px-3 py-2 text-xs text-blue-800">
                                        {autoGeneratedValueHint}
                                    </div>
                                )}
                            </>
                        )}
                    </div>
                </Modal>}
            <table className="appearance-none w-full max-w-100">
                <thead className="border-y bg-gray-50">
                <tr>
                    <th scope="col"
                        className="text-md font-semibold  text-slate-800 md:px-6 py-2 text-left">
                        <div>
                            <div>{title}</div>
                            {description && (
                                <div className="mt-1 text-xs normal-case font-normal tracking-normal text-gray-500">
                                    {description}
                                </div>
                            )}
                        </div>
                    </th>
                    <th scope="col"
                        className="text-sm font-medium text-gray-900 md:px-6 py-2 text-left">

                    </th>
                    <th scope="col"
                        className="text-sm font-medium text-gray-900 md:px-6 py-2 text-right">
                        {
                            keyOptions?.length != 0 &&
                            <div onClick={() => setMode("ADD")}
                                 className={"text-green-600 rounded w-5 h-5"}><HiPlusCircle
                                size={25}/></div>
                        }

                    </th>
                </tr>
                <tr>
                    <th scope="col"
                        className="text-sm font-medium text-gray-900 md:px-6 py-2 text-left">
                        {keyLabel}
                    </th>
                    <th scope="col"
                        className="text-sm font-medium text-gray-900 md:px-6 py-2 text-left">
                        {valueLabel}
                    </th>
                    <th scope="col"
                        className="text-sm font-medium text-gray-900 md:px-6 py-2 text-left">

                    </th>
                </tr>
                </thead>
                <tbody>
                {
                    values && values.length > 0 ?
                        values?.map((i) => (
                            <tr key={`${i.key}_${i.value}`} className="bg-white border-b">
                                <td
                                    className="text-sm text-gray-900 font-light md:px-6 py-4 whitespace-nowrap">
                                    {i.key}
                                </td>
                                <td
                                    className="text-sm w-100 text-gray-900 font-light md:px-4 py-4 break-all">
                                    {(i.value === PRIVATE_SENTINEL || privateKeys?.has(i.key))
                                        ? <span className="inline-flex items-center gap-1.5 text-gray-400 text-xs">
                                            <MdLock size={13}/>
                                            <span className="tracking-widest">••••••••</span>
                                          </span>
                                        : i.value
                                    }
                                </td>
                                <td
                                    className="text-sm text-gray-900 flex items-center font-light md:px-6 py-4 ">
                                    {
                                        onEdit && <Button variant={"none"} onClick={() => {
                                            onPressEdit(i)
                                        }}>
                                            <MdModeEditOutline className={"text-yellow-300"} size={21}/>
                                        </Button>
                                    }

                                    <Button
                                        variant={"none"}
                                        onClick={() => onRemove!(i)}
                                    >
                                        <MdOutlineRemoveCircle className={"text-primary-600"} size={21}/>
                                    </Button>
                                </td>


                            </tr>
                        )) : <tr>{((!hideEmptyLabel) &&
                            <td colSpan={3} className={"md:px-4 py-3"}>No {title}</td>)}
                        </tr>
                }
                </tbody>
            </table>
        </div>
    );
};

export default Component;
